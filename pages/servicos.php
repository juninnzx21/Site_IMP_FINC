                    </p>
                    <p>
                        5. <b>Liberação do recurso:</b> a liberação do crédito pode variar de acordo com a política de
                        crédito do agente financeiro. Os processos de envio da documentação, análise de crédito, análise
                        jurídica e a vistoria são decisivos para o prazo.
                    </p>
                    <p>
                        <b>Como funciona o refinanciamento de <u>empréstimo consignado?</u></b>
                    </p>
                    <p>
                        O refinanciamento de <u><b>empréstimo consignado</b></u> funciona de forma diferente. A operação
                        pode ser feita por aposentados, pensionistas, servidores públicos e trabalhadores de empresas
                        privadas.
                    </p>
                    <p>
                        Ao refinanciar o contrato de empréstimo consignado, o saldo devedor atual é quitado e a
                        diferença é
                        liberada como novo saldo, em conta. Entenda no exemplo abaixo:
                    </p>
                    <p>
                        Suponhamos que você precise de 1 500 reais e o seu saldo devedor atual é de 3 500 reais em 12
                        parcelas restantes. Nesse caso, é necessário consultar a sua margem consignável e verificar a
                        disponibilidade para contratar o novo valor.
                    </p>
                    <p>
                        Em caso afirmativo, o agente financeiro quita as parcelas restantes e disponibiliza um novo
                        empréstimo no valor de 5 000 reais. Um novo contrato de empréstimo é firmado e o consumidor fica
                        com
                        a diferença de 1 500 reais, o chamado “troco”, que é liberado na conta indicada no prazo de 2 a
                        5
                        dias úteis, em média.
                    </p>
                    <p>
                        Ou seja, quanto mais parcelas tiverem sido pagas, maior o valor (limite) liberado. Os valores
                        liberados sempre serão proporcionais ao que já foi pago do empréstimo atual.
                    </p>
                    <p>
                        <b>Como funciona o refinanciamento de imóvel?</b>
                    </p>
                    <p>
                        O <u><b>refinanciamento de imóvel</b></u>, ou <u><b>empréstimo com imóvel em garantia</b></u>, é
                        semelhante ao processo realizado com veículos. Por envolver uma propriedade, a análise de
                        documentos
                        é mais robusta e, consequentemente, leva um pouco mais tempo para a liberar o dinheiro.
                    </p>
                    <p>
                        Porém, como o processo envolve um bem sólido e durável, valores liberados e prazos de pagamento
                        são
                        mais altos. Essa linha de crédito é destinada à pessoas que sejam proprietárias de um imóvel e
                        pretendem colocá-lo em garantia para conseguir as melhores condições de pagamento.
                    </p>
                    <p>
                        O refinanciamento de imóvel é muito procurado para investir e aumentar os negócios, além de
                        quitar
                        as dívidas e regularizar a situação financeira.
                    </p>
                    <p>
                        <b>Como contratar? </b>
                    </p>
                    <p>
                        1. <b>Solicitação:</b> você pode fazer a simulação e solicitação de crédito pela internet,
                        preenchendo alguns dados, ou pelo telefone, de acordo com o agente financeiro escolhido.
                    </p>
                    <p>
                        2. <b>Análise de crédito:</b> nessa etapa é avaliado se a quantia solicitada pode ser emprestada
                        de
                        acordo com a sua situação financeira, que deve estar dentro dos critérios da empresa para ser
                        aprovada. Para isso, busca-se seu histórico de crédito e perfil de consumo.
                    </p>
                    <p>
                        3. <b>Análise jurídica e avaliação do imóvel:</b> os documentos de quem solicita passam por uma
                        verificação e triagem. Paralelamente o bem é avaliado para então se tornar garantia do
                        pagamento.
                    </p>
                    <p>
                        4. <b>Assinatura do contrato: </b>depois de tudo aprovado, é liberado o documento de
                        formalização do
                        empréstimo para assinatura, chamado de CCB (Cédula de Crédito Bancário). O bem deve ser
                        cadastrado
                        no Cartório de Registro de Imóveis.
                    </p>

                    </p>
