                    <p class="card-text">
                        <b>O que é empréstimo pessoal ?</b>
                    </p>
                    <p class="text-wrap">

                    <p>
                        O crédito pessoal nada mais é do que <b>uma determinada quantia, concedida por uma instituição
                            bancária ou financeira, que tem como maior diferencial a liberdade para o seu uso. </b> Isso
                        significa que você não precisa dizer como gastará essa verba, como acontece com os
                        financiamentos.

                    </p>

                    <p>
                        Em troca desse serviço, a instituição cobra juros mensais, que variam de acordo com a negociação
                        firmada em contrato e a política da empresa. Outros detalhes podem interferir nesse valor, como
                        a análise de crédito do cliente e o montante emprestado. Tudo isso pode gerar um reajuste na
                        taxa como forma de evitar a probabilidade de inadimplência.

                    </p>

                    <p>
                        <b>Como o empréstimo pessoal funciona? </b>

                    </p>

                    No <u><b>empréstimo pessoal</b></u>, a instituição empresta o dinheiro para uma pessoa física com
                    posse de documentos de identidade válidos no país. Como não é preciso comunicar o uso dessa quantia,
                    basta negociar as obrigações contratuais, como número de parcelas e percentuais de juros.

                    </p>

                    <p>
                        <b>Após coletar a documentação necessária, a instituição fará uma análise de crédito do
                            solicitante</b>, a fim de saber se ele tem condições de quitar a dívida. Nessa etapa, são
                        analisadas dívidas, hábitos de consumo e outros registros públicos. Se estiver tudo certo, a
                        aprovação será bem rápida, sendo possível receber a quantia no mesmo dia.

                    </p>

                    <p>
                        <b>Por que pedir o crédito pessoal? </b>

                    </p>

                    <p>
                        Existem vários motivos que podem levar você a pedir esse tipo de crédito, como resolver uma
                        emergência de maneira mais rápida possível. Confira!

                    </p>

                    <p>
                        <b>Estudar</b>

                    </p>

                    <p>
                        Está a fim de aprimorar os seus conhecimentos para se destacar no mercado de trabalho e crescer
                        profissionalmente? Então não espere muito e saia na frente dos seus concorrentes! É possível
                        solicitar empréstimo para <b>pagar um curso de idiomas ou uma pós-graduação</b>, por exemplo, e
                        inclusive conseguir crédito suficiente para fazer intercâmbios e impulsionar a sua carreira.

                    </p>

                    <p>
                        O segredo para o sucesso está em não perder a chance de investir no futuro. Certamente, daqui a
                        um tempo, você colherá bons frutos.

                    </p>

                    <p>
                        <b>Viajar</b>

                    </p>

                    <p>
                        Mesmo que existem diversas ofertas no mercado de viagens, um empréstimo pessoal para viajar pode
                        <b>sair bem mais em conta</b> e ainda beneficiar o controle das suas finanças.

                    </p>

                    <p>
                        Da mesma forma que grande parte dos produtos/serviços, um pacote de viagem com passagem e
                        hospedagem pode ter o seu preço duplicado no parcelamento. Nesse sentido, você pode solicitar um
                        crédito com veículo ou imóvel em garantia para economizar nesses momentos de lazer.

                    </p>

                    <p>
                        <b>Fazer festa de aniversário</b>
                    </p>

                    <p>
                        Reunir familiares e amigos para comemorar é ótimo, mas uma festa de aniversário memorável para
                        alguém especial não sai barato.
                    </p>
                    <p>
                        É por isso que um empréstimo pode ser uma solução. Analise o quanto você necessita para fazer
                        um<b> evento completo: </b>
                    </p>

                    <p>
                        -Aluguel de salão;
                    </p>

                    <p>
                        -Buffet;
                    </p>

                    <p>
                        -Música (repertório e DJ);
                    </p>
                    <p>
                        -Decoração;
                    </p>

                    <p>
                        -Fotografia;
                    </p>

                    <p>
                        -Espaço recreativo para crianças (se for o caso).
                    </p>

                    <p>
                        <b>Realizar uma festa de casamento</b>
                    </p>

                    <p>
                        É perfeitamente possível fazer um matrimônio simples e com pouco dinheiro. Mas quando o casal
                        não renuncia a uma boa festa, o céu é limite. A organização desse momento tão especial conta
                        com<b> diversos detalhes. </b> Dúvida? Espie só:
                    </p>
                    <p>
                        -Alimentação;
                    </p>

                    <p>
                        -Bebidas;
                    </p>

                    <p>
                        -Convites;
                    </p>

                    <p>
                        -Doces e salgados;
                    </p>

                    <p>
                        -Decoração;
                    </p>

                    <p>
                        -Cerimônias (civil e religiosa);
                    </p>

                    <p>
                        -Fotos e vídeos;
                    </p>

                    <p>
                        -Roupas dos noivos;
                    </p>

                    <p>
                        -Dia da noiva;
                    </p>

                    <p>
                        -Lua de mel.
                    </p>

                    <p>
                        Acima, foram mencionados somente os principais itens, mas a lista pode ficar ainda mais extensa
                        adicionando estacionamento, segurança, lembrancinhas, acessórios para a pista de dança,
                        presentes para padrinhos e pais, entre muitos outros.
                    </p>

                    <p>
                        A grande vantagem de obter um empréstimo pessoal para casamento é que você terá a quantia
                        necessária em mãos. Não será necessário parcelar nada, o que pode favorecer descontos
                        tentadores. Ou seja, você evita uma dívida cara e obtém um crédito mais conveniente.
                    </p>

                    <p>
                        <b>Redecorar o imóvel</b>
                    </p>

                    <p>
                        Está a fim de repaginar o ambiente e o visual da sua casa? Isso requer dedicação e
                        investimentos. Você vai precisar de uma <b>boa quantia</b> para comprar novos móveis e objetos
                        decorativos. Nesse caso, é recomendado contratar um profissional especializado. Com o empréstimo
                        pessoal, o seu sonho estará a um passo da realidade. O seu conforto e bem-estar merecem esse
                        investimento.
                    </p>

                    <p>
                        <b>Aumentar o rendimento</b>
                    </p>

                    <p>
                        Em geral, créditos pessoais são vistos como desperdício de dinheiro, mas essa é uma perspectiva
                        muito pessimista e injusta.
                    </p>

                    Imaginemos, por exemplo, a condição de um microempresário — mesmo que ele ofereça bons
                    produtos/serviços, um marketing convincente e consiga gerar boas vendas, ele não pode pensar em
                    ampliar o negócio sem ter dinheiro suficiente para fazer <b>investimentos de longo prazo. </b>Com o
                    empréstimo, ele consegue adquirir bons equipamentos para aumentar a sua produção e gerar mais
                    empregos.
                    </p>
                    <p>
                        <b>Fazer uma obra emergencial em casa</b>
                    </p>

                    <p>
                        Seu imóvel necessita de uma boa reforma, mas você não tem dinheiro suficiente para restaurar o
                        que precisa? Faça as contas para descobrir o valor necessário. Para isso, <b>pesquise a mão de
                            obra e os materiais necessários em várias lojas. </b>Com o dinheiro em mãos, fale que
                        pretende pagar à vista e escolha o estabelecimento que conceder o melhor desconto.
                    </p>

                    <p>
                        <b>Fazer um tratamento médico</b>
                    </p>
                    <p>
                        Se você ou alguém próximo precisa de uma intervenção médica urgente e você não tem como pagar
                        essa conta, fazer um empréstimo para essa situação emergencial pode evitar situações como a
                        descontinuação do tratamento. Assim, <b>você paga a emergência à vista </b>e fica com as
                        mensalidades para quitar o serviço prestado.
                    </p>

                    <p>
                        <b>Pagar a academia</b>
                    </p>

                    <p>
                        Parcelar a academia pode não ser uma boa opção quando o pagamento é feito no cartão de crédito,
                        pois o seu limite é comprometido. Ao contratar um <b>plano semestral ou anual, </b> você
                        incentiva a ida à academia. Sem falar no desconto que pode ser conseguido.
                    </p>

                    <p>
                        <b>Limpar o nome no mercado</b>
                    </p>

                    <p>
                        O acúmulo de contas não pagas pode levar à <b>negativação do CPF. </b> Além do incômodo, essa
                        condição pode impossibilitar boas oportunidades, como um financiamento de carro ou imóvel. Com o
                        crédito concedido por uma instituição financeira, é possível pagar os débitos e vencer esse tipo
                        de inadimplência.
                    </p>

                    <p>
                        <b>Como se planejar para pegar um crédito pessoal? </b>
                    </p>

                    <p>
                        Embora seja um recurso de fácil acesso, o empréstimo pessoal precisa ter um planejamento
                        financeiro bastante adequado, pois o objetivo do solicitante é resolver emergências. Por isso, é
                        necessário considerar alguns fatores antes de solicitá-lo.
                    </p>

                    <p>
                        Esse tipo de crédito não obriga a pessoa dar satisfação quanto à sua finalidade para a
                        instituição, mas <b>você deve ter os motivos devidamente definidos na sua mente. </b> Isso soa
                        um pouco óbvio. Afinal, é impossível saber a quantia se não existir um objetivo definido.
                    </p>

                    <p>
                        Se o empréstimo é para comprar um carro, pesquise o modelo ideal para o seu perfil e o quanto
                        ele custará. Ao levantar esses detalhes, você certamente fará um empréstimo consciente, sem
                        pedir um valor desnecessário.
                    </p>

                    <p>
                        Como o crédito precisa ser pago todo mês, entenda que uma fatia do seu orçamento será
                        direcionada para esse pagamento. Portanto, na hora de fechar negócio, veja se é possível arcar
                        com esse tipo de acréscimo sem afetar as suas economias.
                    </p>

                    <p>
                        Veja algumas dicas:
                    </p>

                    <p>
                        -Pagar as parcelas na data do vencimento;
                    </p>

                    <p>
                        -Pagar antecipadamente sempre que puder;
                    </p>

                    <p>
                        -Se tiver valor suficiente em mãos, liquidar o empréstimo;
                    </p>

                    <p>
                        -Não deixar de pagar outros custos e despesas.
                    </p>

                    <p>
                        Essas práticas deixarão você livre de compromissos financeiros o mais breve possível. <b>Ao
                            atingir esse nível de estabilidade, você estará pronto para solicitar novos créditos, caso
                            haja necessidade. </b>
                    </p>

                    <p>
                        Além disso, pense sempre que os períodos de contrato com a instituição que concede o empréstimo
                        são passageiros. Apenas porque você optou por esse recurso para resolver uma situação urgente
                        não quer dizer que você ficará pagando parcelas de crédito pessoal pelo resto da vida.
                    </p>

                    <p>
                        <b>Quais são os tipos de empréstimo pessoal existentes? </b>
                        Existem diversas formas de solicitar o crédito pessoal. Veja as principais a seguir.
                    </p>

                    <p>
                        <b>Consignado</b>
                    </p>

                    <p>
                        O empréstimo consignado tem uma característica interessante. Ele costuma ter juros reduzidos em
                        relação às outras modalidades de crédito. Isso ocorre porque <b>as suas parcelas são retiradas
                            do pagamento do contratante. </b>
                    </p>
                    <p>
                        Devido às suas condições, a modalidade consignada é uma das melhores alternativas para quem
                        precisa de dinheiro para comprar algum bem ou serviço de alto valor ou cobrir uma emergência.
                    </p>

                    <p>
                        Esse tipo de crédito é indicado para quatro grupos de pessoas:
                    </p>

                    <p>
                        -Aposentados e pensionistas do INSS;
                    </p>

                    <p>
                        -Funcionários públicos;
                    </p>

                    <p>
                        -Pessoas empregadas que tenham saldo em conta FGTS;
                    </p>

                    <p>
                        -Empregados de empresas privadas que tenham contrato firmado com instituições financeiras.
                    </p>

                    <p>
                        Sendo assim, é importante que você se enquadre em, pelo menos, uma dessas categorias para ter
                        direito ao empréstimo consignado.
                    </p>

                    <p>
                        <b>Penhora de bens</b>
                    </p>

                    <p>
                        A penhora de bens é um recurso de <b>concessão de crédito baseado em garantia. </b> Isso
                        significa que podem ser oferecidos objetos, como carros, imóveis e joias. Tais pertences serão
                        analisados pela instituição financeira com o auxílio de uma pessoa qualificada. Os seus valores
                        serão definidos e assim é liberado o empréstimo com penhor.
                    </p>

                    <p>
                        Vale mencionar que essa é uma modalidade em que <b>o bem pode ser tomado do solicitante do
                            crédito, em casos de inadimplência. </b>Outro fator merece ser esclarecido é que a avaliação
                        do bem pode dar um valor abaixo do que ele realmente vale.
                    </p>

                    <p>
                        No caso de empréstimos feitos com penhor de joias, o crédito pode ser liberado mesmo com
                        negativação nos órgãos de proteção ao crédito. Quanto aos carros e imóveis, uma pesquisa de
                        restrições será necessária.
                    </p>
                    <p>
                        Embora seja tentadora — por causa das taxas de juros mensais baixíssimas —, essa modalidade de
                        empréstimo deverá ser tomada com certo cuidado, visto que o não pagamento das parcelas, após 30
                        dias do seu vencimento, podem levar o patrimônio penhorado a leilão. Além disso, caso a pessoa
                        venda o bem, o dinheiro será destinado para o pagamento do crédito pessoal.
                    </p>

                    <p>
                        Apesar da garantia do bem, ainda é preciso comprovar renda, para deixar claro que a pessoa terá
                        condições de arcar com os custos mensais. É preciso ter um bom planejamento financeiro antes de
                        solicitar esse tipo de empréstimo para não correr riscos.
                    </p>

                    <p>
                    <p>
                        <b>Cartão de crédito rotativo</b>
                    </p>

                    <p>
                        Quem tem cartão de crédito tem direito a duas alternativas quando chega a fatura mensal:
                        <b>pagar o “mínimo” estabelecido ou o total. </b> Quando você decide pela primeira opção, ocorre
                        uma espécie de empréstimo automático, ou seja, você pega o valor restante da fatura com a sua
                        operadora.
                    </p>

                    <p>
                        Vamos a um exemplo prático. Imagine que a sua fatura veio R$1.200,00 e pagamento mínimo (15%)
                        seria de R$180,00. Se você desembolsar somente os 15%, pegaria emprestado R$1.080,00, que seriam
                        cobrados na fatura do próximo mês, com os juros definidos pelo mercado.
                    </p>

                    <p>
                        Essa forma de concessão de crédito não é nada vantajosa, pois <b>as taxas cobradas são
                            altíssimas </b>e podem fazer você perder o controle do seu orçamento. Faça um esforço para
                        pagar sempre o total gasto com o cartão.
                    </p>

                    <p>
                        <b>Cheque especial</b>
                    </p>

                    <p>
                        Esse é um recurso bastante parecido com o empréstimo pessoal convencional, no tocante às
                        vantagens e desvantagens. No entanto, a maior diferença é que <b>o cheque especial não exige
                            contratação. </b>
                    </p>

                    <p>
                        Em geral, as instituições bancárias já o liberam como limite embutido, podendo ser utilizado
                        quando o cliente desejar. Contudo, é preciso prestar atenção aos juros cobrados, pois costumam
                        ser bastante elevados.
                    </p>

                    <p>
                        <b>Crédito pessoal pela internet</b>
                    </p>

                    <p>
                        Esse é um tipo de crédito bem recente. Ele tem muitas semelhanças com o empréstimo pessoal
                        convencional. O diferencial é que é ofertado por empresas que atuam exclusivamente pela
                        internet. <b>Todo o processo é feito online, sem que seja necessário se deslocar fisicamente até
                            o local. </b>
                    </p>

                    <p>
                        Quanto aos juros, eles costumam ser bem acessíveis em algumas empresas. Isso porque a operação é
                        feita estritamente pela rede mundial de computadores, fazendo com que essas marcas não tenham
                        custos relacionados a aluguel e manutenção de agências físicas.
                    </p>

                    <p>
                        No entanto, existe uma desvantagem: essas instituições são mais exigentes em relação às análises
                        de crédito para liberar o crédito pessoal.
                    </p>

                    <p>
                        <b>Quais são as principais diferenças entre os tipos de empréstimo? </b>
                    </p>

                    <p>
                        Agora que já conhecemos os principais tipos de crédito ofertados no mercado, conheça a seguir as
                        maiores diferenças entre cada um.
                    </p>

                    <p>
                        <b>Juros</b>
                    </p>

                    <p>
                        O maior diferencial de um empréstimo é a taxa de juros, pois ela influencia diretamente o quanto
                        será pago por esse crédito. Confira a comparação aproximada de juros entre algumas modalidades,
                        com base nos dados do <u><b>Banco Central do Brasil: </b></u>
                    </p>

                    <p>
                        <b>-Empréstimo consignado: </b>2,02% a.m.;
                    </p>

                    <p>
                        <b>-Empréstimo pessoal: </b>3,69% a.m.;
                    </p>

                    <p>
                        <b>-Cartão de crédito rotativo: </b>14,35 a.m.;
                    </p>

                    <p>
                        <b>-Cheque especial: </b> 12,18% a.m.;
                    </p>

